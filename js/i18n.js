  function interpolate(str, vars) {
    if (!vars) return str;
    return str.replace(/\{(\w+)\}/g, (_, key) => vars[key] != null ? vars[key] : `{${key}}`);
  }

  const i18n = {
    current: 'en',

    set(lang) {
      if (STRINGS[lang]) this.current = lang;
    },

    t(key, vars) {
      const dict = STRINGS[this.current] || STRINGS.en;
      const str = dict[key] || STRINGS.en[key] || key;
      return interpolate(str, vars);
    },

    direction(code) {
      return (DIRECTIONS[this.current] || DIRECTIONS.en)[code] || code;
    },

    // Returns a localized name from a multilingual name object
    name(obj) {
      if (typeof obj === 'string') return obj;
      return obj[this.current] || obj.en || Object.values(obj)[0];
    },

    languages: ['en', 'zh', 'ja', 'ko']
  };

  window.zirizimaI18n = i18n;
